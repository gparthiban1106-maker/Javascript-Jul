//While Syntax - Example

let value=1;

while (value<=5){
    console.log("Number is",value);
    value++;
}

//do while syntax, example
let value2=6;

do{
    console.log("Number is:",value2);
    value2++;
}while(value2<=5);

//for loop

for(let i=0; i<5; i++){
    console.log("Counting the Loop",i);
    
}